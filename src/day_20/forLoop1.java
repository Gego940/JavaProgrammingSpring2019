package day_20;

public class forLoop1 {
public static void main(String[] args) throws InterruptedException {
	for (int i = 1; i<=10; i++) {
		System.out.println("i love java");
	}
	
	
	
	for(int i=5; i<=15; i++) {
	System.out.println( i );
	System.out.println("");
}
	
	
	for (int i = 20; i>=10; i--) {
		System.out.println(i);
		Thread.sleep(1000);
	}
}
}